    out.push_str("    set = ");
    out.push_str(&c_retain_ident(&set_type));
    out.push_str("(set);\n");
    out.push_str("    if (!nomo_collections_set_contains(set, value)) {\n");
    out.push_str("        set.");
    out.push_str(&values);
    out.push_str(" = ");
    out.push_str(&string_array);
    out.push_str("_push(set.");
    out.push_str(&values);
    out.push_str(", value);\n");
    out.push_str("    }\n");

    out.push_str(".len; i += 1) {\n");
    out.push_str("        if (nomo_string_equal(map.");
    out.push_str(&keys);
    out.push_str(".data[i], key)) { return 1; }\n");
    out.push_str("    }\n");
    out.push_str("    return 0;\n");
    out.push_str("}\n\n");

